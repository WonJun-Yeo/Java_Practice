package Practice;

public class JavaNamingConvention {

	public static void main(String[] args) {
		// 변수명
		boolean aBcD;
		byte 가나다;
		char $ab_ad;
		long abcd3;
		String myCase;
		
		// 상수명
		final double PI;
		final int MY_DATA;
	}
	
	// 메서드명
	void aBcD() {}
	void 가나다() {}
	void $ab_de() {}
	void abcd3() {}
	void myCase() {}
}
