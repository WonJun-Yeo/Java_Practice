package kosmo_ywj;

public class variableAndDataTypeDeclaration {

	public static void main(String[] args) {
		// 자료형 선언
		int a;				// 자료형이 int인 값만 할당가능
		String b;			// 자료형이 String인 값만 할당가능
		double c;			// 자료형이 double인 값만 할당가능
		
		// 변수선언 및 값할당
		int d = 3;
		String e = "안녕";
		double f = 5.3;
		
		// 변수선언과 값할당 따로
		int g;
		g = 2;
		
		String h;
		h = "반가워";
		
		double i;
		i = 2.2;

	}

}
