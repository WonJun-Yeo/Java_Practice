package chap01;
public class Ex01_ConsoleOut {
	public static void main(String[] args) {
		System.out.println("출력을 처리하고 엔터가 들어감"); //줄바꿈됨 "ln"은 Line의 줄임말
		System.out.println("=========================");
		System.out.print("화면");
		System.out.print("출력");
		System.out.println("=========================");
		System.out.print("화면\n");       // "\n"은 줄바꿈을 뜻함
		System.out.print("출력");
	}
}
